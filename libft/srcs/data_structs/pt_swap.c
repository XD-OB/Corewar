#include "libft.h"

void			pt_swap(t_point *p1, t_point *p2)
{
	t_point		tmp;

	tmp = pt_new(p1->y, p1->x);
	p1->y = p2->y;
	p1->x = p2->x;
	p2->y = tmp.y;
	p2->x = tmp.x;
}
